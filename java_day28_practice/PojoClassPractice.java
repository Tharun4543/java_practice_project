package java_day28_practice;

public class PojoClassPractice {
	
	private int id;
	public String name;
	protected float salary;
	
	public void setId(int id)
	{
		this.id = id;
	}
	public int getId()
	{
		return id; 
	}
	public String getName()
	{
		return name;
	}
	public float getSalary()
	{
		return salary;
	}

}
